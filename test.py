import unittest
import GameLib as g

class TestGame(unittest.TestCase):
    def test_first(self):
        self.assertEqual(g.game(1, [['r', 'r', 'f', 'r', 's'],
                                    ['f', 'r', 'f', 'r', 's'],
                                    ['f', 'r', 'f', 'r', 's'],
                                    ['f', 'r', 'f' , 'r', 'r']]), [['.', '.', '.', '.', '.', '.', '.'],
                                                                  ['.', 'r', 'r', '.', 'r', '.', '.'],
                                                                  ['.', '.', 'r', 'f', 'r', 's', 's'],
                                                                  ['f', 'f', 'r', 'f', 'r', '.', '.'],
                                                                  ['.', '.', 'r', '.', 'r', 'r', '.'],
                                                                  ['.', '.', '.', '.', '.', '.', '.']])

    def test_second(self):
        self.assertEqual(g.game(1, [['f', 'f', 'f'],
                                    ['f', 'f', 'f'],
                                    ['f', 'f', 'f']]), [['.', '.', 'f', '.', '.'],
                                                        ['.', 'f', '.', 'f', '.'],
                                                        ['f', '.', '.', '.', 'f'],
                                                        ['.', 'f', '.', 'f', '.'],
                                                        ['.', '.', 'f', '.', '.']])
    def test_third(self):
        self.assertEqual(g.game(2, [['f', 'f', 'f'],
                                    ['f', 'f', 'f'],
                                    ['f', 'f', 'f']]), [['.', '.', '.', '.', '.', '.', '.'],
                                                        ['.', '.', '.', 'f', '.', '.', '.'],
                                                        ['.', '.', 'f', 'f', 'f', '.', '.'],
                                                        ['.', 'f', 'f', '.', 'f', 'f', '.'],
                                                        ['.', '.', 'f', 'f', 'f', '.', '.'],
                                                        ['.', '.', '.', 'f', '.', '.', '.'],
                                                        ['.', '.', '.', '.', '.', '.', '.']])

